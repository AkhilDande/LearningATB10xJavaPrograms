package ex_04_Operators;

public class Lab033_UnaryOperator {
    public static void main(String[] args) {
        int a = +10; // Unary operator is operator where we will be using for only one operand.
        int b = -110;
        System.out.println(a);
        System.out.println(b);
        System.out.println(a+b);
    }
}
