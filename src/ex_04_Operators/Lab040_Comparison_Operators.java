package ex_04_Operators;

public class Lab040_Comparison_Operators {
    public static void main(String[] args) {
        System.out.println(10 == 10); // Compariosion operators Equal to (==)
        System.out.println(10 == 20); // Compariosion operators Equal to (==)
        System.out.println(10 >= 20); // Compariosion operators greater than or Equal to (>=)
        System.out.println(10 <= 20); // Compariosion operators less than or Equal to (>=)
        System.out.println(10 >= 10); // Compariosion operators greater than or Equal to (>=)
    }
}
