package ex_04_Operators;

public class Lab032_Operators_Arithematic {
    public static void main(String[] args) {
        int a = 3;
        int b = 2;
        float c = 9.14f;
        // Arithmetic Operators
        //        + (Addition)
//                - (Subtraction)
//                * (Multiplication)
//                / (Division) -> Quotient
//                % (Modulo) | Modulus -> Remainder
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(c/a);
    }
}
