package ex_01_Java_Basic;

public class Lab008_Keywords_Identifiers {
    public static void main(String[] args) {
        System.out.println("All the words highlighted in orange in this program are keywords.keywords which are having predefined meaning in Java language");
        System.out.println("Unique Name given to any programming elements like classes, Methods and variables ex:Lab008_Keywords_Identifiers in this code");
        System.out.println("Rule1. to follow when defining Identifier: Can contain Alphabets, digits, only two special characters ex:$,_");
        System.out.println("Rule2. Underscore (_) is also an identifier");
        System.out.println("Rule3. First character of an identifier can be only Alphabet or $ or _ But cannot start with digit (0-9)");
        System.out.println("Rule4. Keywords and reserved words (Ex:class,static, void, public) cant be used as Identifiers");
    }
}
