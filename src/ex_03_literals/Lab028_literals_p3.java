package ex_03_literals;

public class Lab028_literals_p3 {
    public static void main(String[] args) {
        int Enum = 100;
        // We can use varaiable name as "Enum" as it is starting with 'E'(Upper case)
        // It is different if we use variable name as "enum" as it is starting with 'e'(Lower case) which is considered as keyword
        System.out.printf("the value of Enum is %d\n", Enum);
        System.out.println(Enum);
    }
}
