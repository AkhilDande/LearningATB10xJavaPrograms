package ex_03_literals;

public class Lab027_Booleanlliterals {
    public static void main(String[] args) {
        int a = 100;
        int b = 105;
        boolean c = a>b; //Boolean literals are either true or false
        boolean d = a<b;
        boolean e = true;
        boolean f = false;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
