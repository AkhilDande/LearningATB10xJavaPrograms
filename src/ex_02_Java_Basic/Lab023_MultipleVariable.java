package ex_02_Java_Basic;

public class Lab023_MultipleVariable {
    public static void main(String[] args) {
        int a = 10, b = 20, c = 30;
        int d = 40;
        //Multiple Variables cannot be initialized in single line separately with semi columns
        // Ex: int a = 1;b =2; c=3;
        // int e = 1; f = 2; g = 3;
        int x = 1; int y =2; int z =3;
        System.out.println(a+b+c+d+x+y+z);
    }
}
