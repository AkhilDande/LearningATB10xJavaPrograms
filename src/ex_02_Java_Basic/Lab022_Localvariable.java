package ex_02_Java_Basic;

public class Lab022_Localvariable {
    public static void main(String[] args) {
        int a = 10;
        float b = 3.14f;
        double c = 1.389;
        char d = 'A';
        String e = "Hello World";
        byte f = 10;
        short g = 20;
        long h = 30;
        boolean i = true;
        System.out.println("Local variables are variables which are initialized within Main method(Static method)");
        System.out.println(a+h+f+h+c+b);
    }
}
