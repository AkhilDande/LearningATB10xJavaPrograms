package Task11thJan2025;

public class printf_Tableof9 {
    public static void main(String[] args) {
        System.out.printf("9*1=%d\n", 9 * 1); // to print a statement in next line add \n before the string ends so that next statements gets printed in next line
        System.out.printf("9*2=%d\n", 9 * 2);  // use the %d to print the mathematical result inside the string and the input, after comma ex: 9*2 in this line
        System.out.printf("9*3=%d\n", 9 * 3);  // Format of printf -> System.out.printf(locale, formatString, args)
        System.out.printf("9*4=%d\n", 9 * 4);  // formatString = A string containing placeholders for the additional arguments indicating how to format them
        System.out.printf("9*5=%d\n", 9 * 5);   // For Instance: System.out.printf("9*1=%d\n", 9 * 1);
        System.out.printf("9*6=%d\n", 9 * 6);   // In the Above instance "9*1=%d\n" is the format of the string & "9*1" is the argument which is replaced by %d
        System.out.printf("9*7=%d\n", 9 * 7);
        System.out.printf("9*8=%d\n", 9 * 8);
        System.out.printf("9*9=%d\n", 9 * 9);
        System.out.printf("9*10=%d\n", 9 * 10);
    }
}

