package ex_07_Increment_decrement_OP;

public class Lab066_Pre_Inc {
    public static void main(String[] args) {
        int a = 50;
        System.out.println(++a); // As this is pre increment the value will be incremented and assigned to 'a' at thIS step itself. So the value of a will be 51.
        System.out.println(a); // As this is pre increment the value will be incremented and assigned to 'a' 6 step itself.
    }
}
